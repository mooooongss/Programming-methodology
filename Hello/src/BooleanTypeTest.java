public class BooleanTypeTest {
	public static void main(String[] args) {
		boolean a = true;
		System.out.println("boolean 변수 a의 값은 : " + a);
		
		boolean b = 10 > 20;
		System.out.println("boolean 변수 b의 값은 : " + b);
		
		boolean c = a;
		System.out.println("boolean 변수 c의 값은 : " + c);
	}
}