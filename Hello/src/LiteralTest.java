
public class LiteralTest {

	public static void main(String[] args) {
		int a = 100;
		int b = 0b1100100;
		int c = 0144;
		int d = 0x64;
		System.out.println("10진수 100 = " + a);
		System.out.println("2진수 100 = " + b);
		System.out.println("8진수 100 = " + c);
		System.out.println("16진수 100 = " + d);
		System.out.println("2진수 100 = " + 0b111);
		System.out.println("8진수 100 = " + 0777);
		System.out.println("16진수 100 = " + 0xfff);
	}

}
